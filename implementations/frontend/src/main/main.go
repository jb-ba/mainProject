package main

import (
	"github.com/gin-gonic/contrib/static"
	"github.com/gin-gonic/gin"
)

func main() {
	r := gin.Default()
	r.GET("/ping", func(c *gin.Context) {
		c.JSON(200, gin.H{
			"message": "pong",
		})
	})
	r.Use(static.Serve("/", static.LocalFile("../../website/build", true)))
	r.Run() // listen and serve on 0.0.0.0:8080
}
